import * as fs from "fs";
import * as path from "path";
import inquirer from "inquirer";
import chalk from "chalk";
import { execa } from "execa";

// Defines the structure for the login result
export interface LoginResult {
	success: boolean;
	token?: string;
}
const REPO_URL_SSH = "git@github.com:signaloid/Signaloid-Demo-API-AutogeneratedTemplate.git";

let cachedToken: string | undefined = undefined;
/**
 * Logs the user into the GitHub npm registry and configures Git credentials for the project.
 * This method is non-interactive, robust, and suitable for CLI and CI/CD environments.
 *
 * @param outPath The directory where the .netrc and .npmrc files will be created.
 * @returns A promise that resolves with an object containing the success status and the token.
 */
export async function loginAndGetToken(outPath: string = process.cwd()): Promise<LoginResult> {
	if (!cachedToken) {
		console.log(
			chalk.blue(
				"To clone the private repository and install packages, you need a GitHub Personal Access Token.",
			),
		);
		console.log(chalk.blue("Please create one at https://github.com/settings/tokens with the following scopes:"));
		console.log(chalk.yellow("- read:packages (for npm)"));
		console.log(chalk.yellow("- repo (for git clone)"));
		const { token } = await inquirer.prompt([
			{
				type: "password",
				name: "token",
				message: "Enter your GitHub Personal Access Token:",
				mask: "*",
				validate: (input: string) => {
					if (!input) {
						return "Token cannot be empty.";
					}
					// Regex for classic (ghp_) and fine-grained (github_pat_) tokens
					const githubTokenRegex = /^(ghp_[a-zA-Z0-9]{36}|github_pat_[a-zA-Z0-9_]{84})$/;
					if (!githubTokenRegex.test(input)) {
						return "Invalid GitHub token format. Please check your token and try again.";
					}
					return true;
				},
			},
		]);
		cachedToken = token;
	}
	try {
		// --- .netrc configuration for Git (project-level) ---
		const netrcPath = path.join(outPath, ".netrc");
		const netrcMachine = "github.com";
		const newNetrcEntry = `machine ${netrcMachine}\n  login x-access-token\n  password ${cachedToken}`;

		// For a project-level .netrc, we can just overwrite it.
		await fs.promises.writeFile(netrcPath, newNetrcEntry, { mode: 0o600 });
		console.log(chalk.green(`Successfully configured project-level .netrc for Git authentication!`));

		// --- .npmrc configuration for npm ---
		const npmrcPath = path.join(outPath, ".npmrc");
		const registryUrl = "https://npm.pkg.github.com/";
		const scope = "@signaloid";
		const authTokenLine = `//npm.pkg.github.com/:_authToken=${cachedToken}`;
		const registryLine = `${scope}:registry=${registryUrl}`;
		const headerComment = `# This file was generated by the Signaloid CLI to authenticate with GitHub`;

		let npmrcLines: string[] = [];
		if (fs.existsSync(npmrcPath)) {
			const content = await fs.promises.readFile(npmrcPath, "utf8");
			npmrcLines = content.split("\n").filter((line) => {
				const isAuthLine = line.includes("//npm.pkg.github.com/:_authToken=");
				const isRegistryLine = line.includes(":registry=https://npm.pkg.github.com");
				return (
					!isAuthLine && !isRegistryLine && !line.startsWith("# This file was generated by the Signaloid CLI")
				);
			});
		}

		const newNpmrcContent = [headerComment, ...npmrcLines, registryLine, authTokenLine].join("\n");
		await fs.promises.writeFile(npmrcPath, newNpmrcContent);
		console.log(chalk.green("Successfully configured .npmrc for GitHub Packages!"));

		return { success: true, token: cachedToken };
	} catch (error: any) {
		console.error(chalk.red("Failed to write authentication files:"), error.message);
		return { success: false, token: cachedToken };
	}
}

/**
 * Checks if the user has the necessary GitHub permissions to clone private repositories and install packages.
 * It attempts a dry run of cloning a private repository.
 *
 * @returns A promise that resolves to true if authentication is successful, and false otherwise.
 */
export async function checkGithubAuth(): Promise<boolean> {
	console.log(chalk.blue("Checking GitHub authentication..."));

	try {
		// Attempt to list remote refs of the private repository using SSH.
		// This is a lightweight way to check for read access without cloning the whole repo.
		await execa("git", ["ls-remote", REPO_URL_SSH], { stdio: "ignore" });
		console.log(chalk.green("GitHub SSH authentication successful."));
		return true;
	} catch (error) {
		console.log(chalk.yellow("GitHub SSH authentication failed. Let's try with a Personal Access Token."));

		// If SSH fails, fall back to the interactive token login flow.
		const loginResult = await loginAndGetToken(process.cwd());
		if (!loginResult.success || !loginResult.token) {
			console.error(chalk.red("GitHub login failed. Please check your token and permissions."));
			return false;
		}

		// After getting a token, we need to verify it has the correct permissions.
		// We'll try the `ls-remote` command again, but this time with the token.
		const httpsRepoUrl = `https://x-access-token:${loginResult.token}@github.com/signaloid/Signaloid-Demo-API-AutogeneratedTemplate.git`;

		try {
			await execa("git", ["ls-remote", httpsRepoUrl], { stdio: "ignore" });
			console.log(chalk.green("GitHub token has the required permissions."));
			return true;
		} catch (tokenError) {
			console.error(
				chalk.red(
					"The provided GitHub token is invalid or does not have the required 'repo' and 'read:packages' scopes.",
				),
			);
			return false;
		}
	}
}
